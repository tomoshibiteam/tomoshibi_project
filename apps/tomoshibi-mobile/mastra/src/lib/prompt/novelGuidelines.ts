export const NOVEL_CORE_GUIDELINES = `
## 小説品質ルール（要約版）

### 章設計の必須条件
- 各スポットは「章」として設計する
- 毎章に **衝突・葛藤・変化** があること
- 同じ展開の繰り返しは禁止（導入/進行/締め方を変える）

### 70/30ルール
- 70%: 行動・対話・反応
- 30%: 描写・背景・情報整理

### Show, Don’t Tell
- 感情は動作で示す（例: 「怒っている」ではなく「拳を握る」）
- 抽象語より具体語を優先

### 人間らしさの必須要素（各スポットで最低1つ）
- 個人的な細部（癖・持ち物・思考の脱線）
- 不完全さ（迷い、判断の揺れ、ふとした躊躇）
- 未回収要素（まだ説明しない何か）
- 身体感覚（疲れ、匂い、喉の渇きなど）

### 禁止パターン
- 同じ始まり方の連続
- 全感情を最大強度で表現
- 説明過多
- 完璧すぎるリズム
- 定型自己紹介（「〜だ。この件に詳しい」「〜としてここに来た」など）
- 定型の言い直し（「えっと…言い直させて」等）
`;

